package utp.misiontic2022.c2.p47.reto4.modelo.vo;

public class ConsolidadorLideresCiudadResidencia {
    private String ciudadResidencia;
    private long salarios;

    

    public ConsolidadorLideresCiudadResidencia(){}

    public void setCiudadResidencia(String ciudadResidencia) {
        this.ciudadResidencia = ciudadResidencia;
    }
    public String getCiudadResidencia() {
        return ciudadResidencia;
    }

    public void setSalarios(long salarios) {
        this.salarios = salarios;
    }
    public long getSalarios() {
        return salarios;
    }

    
}
